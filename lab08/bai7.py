def min_max(a, b, c):
    min_number = min(a, b, c)
    max_number = max(a, b, c)
    print("Số nhỏ nhất:", min_number)
    print("Số lớn nhất:", max_number)
a = int(input("Nhập số nguyên thứ nhất: "))
b = int(input("Nhập số nguyên thứ hai: "))
c = int(input("Nhập số nguyên thứ ba: "))
min_max(a, b, c)
