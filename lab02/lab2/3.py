#Viết chương trình cho phép nhập vào thứ (1->7) trong tuần.
# Sau đó cho biết thứ đã nhập có tên là gì và xuất kết quả ra màn hình. (1: Sunday, 2: Monday, …)
a=int(input("nhập vào thứ từ thứ 2 đến thứ 7:"))
if a==1:
    print("monday")
elif a==2:
    print("tuesday")
elif a==3:
    print("wednesday")
elif a==4:
    print("thursday")
elif a==5:
    print("friday")
elif a==6:
    print("saturday")
elif a==7:
    print("sunday")
