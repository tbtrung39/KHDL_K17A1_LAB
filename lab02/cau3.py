ngay = int(input("Nhập vào một số từ 1 đến 7: "))

if ngay == 1:
    print("Thứ hai")
elif ngay == 2:
    print("Thứ ba")
elif ngay == 3:
    print("Thứ tư")
elif ngay == 4:
    print("Thứ năm")
elif ngay == 5:
    print("Thứ sáu")
elif ngay == 6:
    print("Thứ bảy")
elif ngay == 7:
    print("Chủ nhật")
else:
    print("Số bạn nhập không hợp lệ, vui lòng nhập lại từ 1 đến 7")