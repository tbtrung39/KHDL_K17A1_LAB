tong_ket = float(input("nhap diem tong ket:"))
if tong_ket > 0 and tong_ket < 3:
    print("kem")
elif tong_ket == 4:
    print("yeu")
elif tong_ket > 5 and tong_ket < 6:
    print("trung binh")
elif tong_ket > 7 and tong_ket < 8:
    print("kha")
elif tong_ket > 8 and tong_ket < 9:
    print("gioi")
elif tong_ket > 9 and tong_ket < 10:
    print('xuat sac')